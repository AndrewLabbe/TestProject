
public class Snack {
	
	private String Name;
	private double Price;
	
	public Snack(String name, double price) {
		Name = name;
		Price = price;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public double getPrice() {
		return Price;
	}

	public void setPrice(double price) {
		Price = price;
	}
	
}
